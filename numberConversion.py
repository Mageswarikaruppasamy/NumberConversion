#Coding for number conversion
num=int(input("Enter a number: "))
print("Decimal value of",num,"=",float(num))  #decimal value
print("Binary value of",num,"=",bin(num))  #binary value
print("Octal value of",num,"=",oct(num)) #octal value
print("Hexadecimal value of",num,"=",hex(num)) #hexadecimal value